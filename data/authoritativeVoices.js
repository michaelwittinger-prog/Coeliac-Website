// Helper functions
export function getVoiceById(id) {
  return authoritativeVoices.find(v => v.id === id);
}

export function getVoicesByType(type) {
  return authoritativeVoices.filter(v => v.type === type);
}

export function getVoicesByRegion(region) {
  return authoritativeVoices.filter(v => v.region.includes(region));
}

